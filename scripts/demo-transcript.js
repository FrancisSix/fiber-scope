import { spawnSync } from 'node:child_process';
import fs from 'node:fs';
import path from 'node:path';

const node = process.execPath;
const transcriptPath = path.resolve('docs/demo-transcript.md');

const commands = [
  {
    title: 'List Fiber node fixtures',
    command: 'npm run fiber-scope -- fixtures',
    args: ['./src/cli.js', 'fixtures']
  },
  {
    title: 'Generate a testnet public-node runbook',
    command: 'npm run fiber-scope -- presets --node fiber-testnet-public-bottle --rpc http://127.0.0.1:8227',
    args: ['./src/cli.js', 'presets', '--node', 'fiber-testnet-public-bottle', '--rpc', 'http://127.0.0.1:8227']
  },
  {
    title: 'Inspect an unbalanced route-failure snapshot',
    command: 'npm run fiber-scope -- inspect --snapshot fixtures/unbalanced-route-failure.json',
    args: ['./src/cli.js', 'inspect', '--snapshot', 'fixtures/unbalanced-route-failure.json']
  },
  {
    title: 'Run a payment-readiness gate',
    command: 'npm run fiber-scope -- gate --snapshot fixtures/healthy-ready.json',
    args: ['./src/cli.js', 'gate', '--snapshot', 'fixtures/healthy-ready.json']
  },
  {
    title: 'Compare fresh node against route-probed node',
    command: 'npm run fiber-scope -- diff --before fixtures/no-peers-no-graph.json --after fixtures/unbalanced-route-failure.json',
    args: ['./src/cli.js', 'diff', '--before', 'fixtures/no-peers-no-graph.json', '--after', 'fixtures/unbalanced-route-failure.json']
  }
];

const lines = [
  '# FiberScope Demo Transcript',
  '',
  'This transcript is generated by `npm run demo:transcript`.',
  ''
];

for (const item of commands) {
  const result = spawnSync(node, item.args, {
    encoding: 'utf8'
  });
  if (result.error) throw result.error;
  if (result.status !== 0) {
    throw new Error(`${item.command} failed:\n${result.stderr || result.stdout}`);
  }

  lines.push(`## ${item.title}`);
  lines.push('');
  lines.push('```bash');
  lines.push(item.command);
  lines.push('```');
  lines.push('');
  lines.push('```text');
  lines.push(trimOutput(result.stdout));
  lines.push('```');
  lines.push('');
}

fs.mkdirSync(path.dirname(transcriptPath), { recursive: true });
fs.writeFileSync(transcriptPath, `${lines.join('\n')}\n`);
console.log(`Wrote ${path.relative(process.cwd(), transcriptPath)}`);

function trimOutput(output) {
  const lines = output.trim().split(/\r?\n/);
  const maxLines = 80;
  if (lines.length <= maxLines) return lines.join('\n');
  return [
    ...lines.slice(0, maxLines),
    `... ${lines.length - maxLines} more lines omitted ...`
  ].join('\n');
}
